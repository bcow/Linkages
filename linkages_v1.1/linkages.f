C                       SUBROUTINE PLOTIN
C
C
C     SUBROUTINE PLOTIN INITIALIZES VARIABLES TO START SIMULATION ON
C     BARE PLOTS.
C     NTREES CONTAINS NUMBER OF TREES FOR EACH SPECIES
C     DBH CONTAINS DIAMETER AT BREAST HEIGHT FOR EACH TREE

         IAGE(K) = IAGE(I)
         NOGRO(K) = NOGRO(I)
   40    CONTINUE
   50 NTOT = K
      IF (NTOT.EQ.0) RETURN
      NTOT1 = K+1
      IF(NTOT1.GT.1500) CALL ERR5
C.....can't get rid of this one...
C.....
C.....ELIMINATE DEAD TREES
C.....
      DO 60 I=NTOT1,NU
         DBH(I) = 0.
         IAGE(I) = 0
         NOGRO(I) = 0
   60    CONTINUE
      RETURN
      END

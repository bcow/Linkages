C.....
C.....CALCULATE TOTAL LITTER (T/HA)
C.....
      TYL(18)=TYL(13)+TYL(14)+TYL(15)+TYL(16)+TYL(17)
C.....
C.....REWRITE DIAMETERS AND AGES TO ELIMINATE DEAD TREES
C.....
